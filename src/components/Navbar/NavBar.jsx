import * as React from "react";
import { styled, alpha } from "@mui/material/styles";
import AppBar from "@mui/material/AppBar";
import Box from "@mui/material/Box";
import Toolbar from "@mui/material/Toolbar";
import Typography from "@mui/material/Typography";
import InputBase from "@mui/material/InputBase";
import SearchIcon from "@mui/icons-material/Search";
import NavMenu from "./NavMenu";
import BreadcrumbText from "../Breadcrumb/BreadcrumbText";
import { useDispatch, useSelector } from "react-redux";
import { setCurrentPath } from "../../features/FileOperations/folderSlice";
import { refreshFileList } from "../../features/FileOperations/fileListSlice";

const Search = styled("div")(({ theme }) => ({
  position: "relative",
  borderRadius: theme.shape.borderRadius,
  backgroundColor: alpha(theme.palette.common.white, 0.15),
  "&:hover": {
    backgroundColor: alpha(theme.palette.common.white, 0.25),
  },
  marginLeft: 0,
  width: "100%",
  [theme.breakpoints.up("sm")]: {
    marginLeft: theme.spacing(1),
    width: "auto",
  },
}));

const SearchIconWrapper = styled("div")(({ theme }) => ({
  padding: theme.spacing(0, 2),
  height: "100%",
  position: "absolute",
  pointerEvents: "none",
  display: "flex",
  alignItems: "center",
  justifyContent: "center",
}));

const StyledInputBase = styled(InputBase)(({ theme }) => ({
  color: "inherit",
  width: "100%",
  "& .MuiInputBase-input": {
    padding: theme.spacing(1, 1, 1, 0),
    // vertical padding + font size from searchIcon
    paddingLeft: `calc(1em + ${theme.spacing(4)})`,
    transition: theme.transitions.create("width"),
    [theme.breakpoints.up("sm")]: {
      width: "12ch",
      "&:focus": {
        width: "20ch",
      },
    },
  },
}));

export default function NavBar() {
  const path = useSelector((state) => {
    return state.folder.currentPath;
  });

  const dispatch = useDispatch();
  function handleClickPath(e, index) {
    e.preventDefault();
    const newPath = [...path].slice(0, index + 1);
    dispatch(setCurrentPath(newPath));
    dispatch(refreshFileList(newPath));
  }
  function handleGoBack() {
    dispatch(setCurrentPath(path.slice(0, -1)));
    dispatch(refreshFileList(path));
  }

  return (
    <Box sx={{ flexGrow: 1 }}>
      <AppBar position="static">
        <Toolbar>
          <Typography
            variant="h6"
            noWrap
            component="div"
            sx={{ flexGrow: 1, display: { xs: "none", sm: "block" } }}
          >
            <BreadcrumbText
              path={path}
              handleClickPath={handleClickPath}
              handleGoBack={handleGoBack}
              rootTitle="Home"
            />
          </Typography>

          <Search>
            <SearchIconWrapper>
              <SearchIcon />
            </SearchIconWrapper>
            <StyledInputBase
              placeholder="Searchâ€¦"
              inputProps={{ "aria-label": "search" }}
            />
          </Search>
          <NavMenu />
        </Toolbar>
      </AppBar>
    </Box>
  );
}
